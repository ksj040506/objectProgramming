public class Account {
    private String name;
    private int balance;

    Account() {}

    public void setName(String name) {
        this.name = name ;
    }

    public void setBalance(int balance) {
        this.balance = balance;
    }

    public String getName() {
        return name ;
    }

    public int getBalance() {
        return balance;
    }


}
